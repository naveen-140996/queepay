    <script>
document.addEventListener("DOMContentLoaded", () => {
    const counters = document.querySelectorAll(".premium-counter");

    // Function to animate counter
    function animateCounter(counter) {
        let duration = parseInt(counter.getAttribute("data-duration"), 10);
        let fromValue = parseInt(counter.getAttribute("data-from-value"), 10);
        let toValue = parseInt(counter.getAttribute("data-to-value"), 10);
        let delimiter = counter.getAttribute("data-delimiter");
        let rounding = parseInt(counter.getAttribute("data-rounding"), 10);

        let startTime = null;

        function startAnimation(currentTime) {
            if (!startTime) startTime = currentTime;
            let timeElapsed = currentTime - startTime;
            let progress = Math.min(timeElapsed / duration, 1);
            let value = Math.floor(fromValue + (toValue - fromValue) * progress);

            // Apply rounding if necessary
            if (rounding > 0) {
                value = Math.round(value / rounding) * rounding;
            }
            
            // Format the value with delimiter
            let formattedValue = value.toString().replace(/\B(?=(\d{3})+(?!\d))/g, delimiter);

            counter.querySelector(".premium-counter-init").textContent = formattedValue;

            if (progress < 1) {
                requestAnimationFrame(startAnimation);
            }
        }

        requestAnimationFrame(startAnimation);
    }

    // Intersection Observer setup
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                // Only start the animation if it hasn't been started
                if (!entry.target.classList.contains('animated')) {
                    animateCounter(entry.target);
                    entry.target.classList.add('animated');
                }
            }
        });
    }, { threshold: 0.5 });

    // Observe all counter elements
    counters.forEach(counter => {
        observer.observe(counter);
    });
});
    </script>
